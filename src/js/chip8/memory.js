  function getState() {
    let state = [];
    getAddressRanges().forEach((blk) => {
      if (!blk.shadow) {
        let memdata = [];
        for (let i = 0; i < blk.size; ++i) {
          memdata.push(read8(blk.start + i, true));
        }

        state.push({
          start: blk.start,
          data: memdata
        });
      }
    });

    return state;
  }

  function setState(json) {
    json.forEach((blk) => {
      let address = blk.start;
      for (let i = 0; i < blk.data.length; ++i, ++address) {
        write8(address, blk.data[i], true);
      }
    });
  }

